package PrimeiroDiaSintaxeVariaveisFluxo;

public class TesteConversoes {
	public static void main(String[] args) {
		double preco = 25.68;
		int valor = (int) preco;
		System.out.println(valor);
		
		long numeroGrande = 4546456465465464l;
		System.out.println(numeroGrande);
		
		short numeroPequeno = 589;
		System.out.println(numeroPequeno);
		
		byte exemploVariavel = 12;
		System.out.println(exemploVariavel);
		
		float exemploPontoFlutuante = 12.58f;
		System.out.println(exemploPontoFlutuante);
	}
}